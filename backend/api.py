import os
import json
from fastapi import FastAPI, UploadFile, File, HTTPException, Depends
from fastapi.middleware.cors import CORSMiddleware
from sqlalchemy import create_engine, Column, Integer, String
from sqlalchemy.orm import declarative_base, sessionmaker, Session
from dotenv import load_dotenv
from google import genai
from google.genai import types
from pydantic import BaseModel

load_dotenv()

# Initialize Gemini Client
api_key = os.getenv("GEMINI_API_KEY")
client = genai.Client(api_key=api_key)

app = FastAPI(title="WasteHunters API")

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_methods=["*"],
    allow_headers=["*"],
)

# ==========================================
# DATABASE SETUP
# ==========================================
SQLALCHEMY_DATABASE_URL = "sqlite:///./wastehunters.db"
engine = create_engine(SQLALCHEMY_DATABASE_URL, connect_args={"check_same_thread": False})
SessionLocal = sessionmaker(autocommit=False, autoflush=False, bind=engine)
Base = declarative_base()

class DBUser(Base):
    __tablename__ = "users"
    id = Column(String, primary_key=True, index=True)
    username = Column(String, unique=True, index=True)
    green_tokens = Column(Integer, default=0)
    total_recycling_events = Column(Integer, default=0)

class DBLesson(Base):
    __tablename__ = "lessons"
    id = Column(Integer, primary_key=True, index=True)
    title = Column(String)
    category = Column(String) 
    content_summary = Column(String)
    video_url = Column(String) 
    quiz_data = Column(String) 

class DBCampaign(Base):
    __tablename__ = "campaigns"
    id = Column(Integer, primary_key=True, index=True)
    title = Column(String)
    location = Column(String)
    date = Column(String)
    volunteers = Column(Integer, default=0)
    max_volunteers = Column(Integer)
    creator = Column(String)

# NEW: Pydantic model for receiving new campaigns from the frontend
class CampaignCreate(BaseModel):
    title: str
    location: str
    date: str
    max_volunteers: int
    creator: str = "GreenHacker" # Defaulting to the logged-in user for the demo

Base.metadata.create_all(bind=engine)

def get_db():
    db = SessionLocal()
    try:
        yield db
    finally:
        db.close()

